    private void btnLexicoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLexicoActionPerformed

        TabelaSimbolos tabelaSimbolos = new TabelaSimbolos();
        AnalisadorLexico analisadorLexico = new AnalisadorLexico(tabelaSimbolos);
        listModel.clear();
        tabelaSimbolos.iniciar();

        analisadorLexico.definirCodigo(txtCodigoFonte.getText());
        while (analisadorLexico.temCodigo()){
            Token token = analisadorLexico.obterToken();
            if (token != null){
               String descricao = Lexema.obterNomeToken(token);
               if (!analisadorLexico.obterLexema().equals(""))
                   descricao += " (lexema "+analisadorLexico.obterLexema()+")";
               listModel.addElement(descricao);
            }
        }

    }//GEN-LAST:event_btnLexicoActionPerformed

    private void btnSintaticoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSintaticoActionPerformed
        TabelaSimbolos tabelaSimbolos = new TabelaSimbolos();
        TratadorErro tratadorErro = new TratadorErro();
        AnalisadorSintatico analisadorSintatico = new AnalisadorSintatico(tabelaSimbolos, tratadorErro);
        NoPrograma programa = analisadorSintatico.executar(txtCodigoFonte.getText());
        listModel.clear();
        ImpressaoArvoreSintatica impressaoArvore = new ImpressaoArvoreSintatica(tabelaSimbolos,listModel);
        impressaoArvore.executar(programa);
        
    }//GEN-LAST:event_btnSintaticoActionPerformed

    private void btnIntermediarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIntermediarioActionPerformed
        TabelaSimbolos tabelaSimbolos = new TabelaSimbolos();
        TratadorErro tratadorErro = new TratadorErro();
        Tradutor tradutor = new Tradutor(tabelaSimbolos, tratadorErro);
        try{
            ArrayList<Instrucao> instrucoes = tradutor.executar(txtCodigoFonte.getText());
            listModel.clear();
            ImpressaoCodigoTresEnderecos impressaoCodigo = new ImpressaoCodigoTresEnderecos(listModel);
            impressaoCodigo.executar(instrucoes);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e.getMessage());
        }

    }//GEN-LAST:event_btnIntermediarioActionPerformed

    private void btnSemanticoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSemanticoActionPerformed
        TabelaSimbolos tabelaSimbolos = new TabelaSimbolos();
        TratadorErro tratadorErro = new TratadorErro();
        AnalisadorSintatico analisadorSintatico = new AnalisadorSintatico(tabelaSimbolos, tratadorErro);
        AnalisadorSemantico analisadorSemantico = new AnalisadorSemantico(tabelaSimbolos, tratadorErro);
        NoPrograma programa = analisadorSintatico.executar(txtCodigoFonte.getText());
        listModel.clear();
        ImpressaoArvoreSintatica impressaoArvore = new ImpressaoArvoreSintatica(tabelaSimbolos,listModel);
        impressaoArvore.executar(programa);
        analisadorSemantico.executar(programa.obterListaComandos());

    }//GEN-LAST:event_btnSemanticoActionPerformed

